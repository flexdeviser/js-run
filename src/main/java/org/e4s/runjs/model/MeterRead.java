package org.e4s.runjs.model;

public class MeterRead {

    private String id;

    private float voltage;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public float getVoltage() {
        return voltage;
    }

    public void setVoltage(float voltage) {
        this.voltage = voltage;
    }
}
